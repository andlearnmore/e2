      <li>I'd like to make a memory-style game.</li>
      <li>(The memory feature will come in at a later stange when people are actually interacting with the app.)</li>
      <li>There will be a 4x4 grid of cards, with eight word pairs.</li>
      <li>Starting with Player A, the app will randomly select two cards. If they match, Player A gets to go again. This continues until they do not match.</li>
      <li>When the cards do not match, Player A's turn ends and it's Player B's turn.</li>
      <li>This continues until there are no pairs left. Whoever has the most pairs, wins. Ties are possible.</li>
    </ul>

